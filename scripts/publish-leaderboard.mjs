#!/usr/bin/env node
import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { repoRoot } from './audit-lib.mjs';

const resultsDirArg = process.argv[2];
if (!resultsDirArg) {
  console.error('Usage: npm run audit:publish -- audits/results/<run-id>/');
  process.exit(1);
}

const resultsDir = path.join(repoRoot, resultsDirArg);
const files = (await readdir(resultsDir)).filter((file) => file.endsWith('.json'));
const results = [];

for (const file of files) {
  const result = JSON.parse(await readFile(path.join(resultsDir, file), 'utf8'));
  results.push(result);
}

results.sort((a, b) => b.average_ccs - a.average_ccs);

const entries = results.map((result, index) => ({
  rank: index + 1,
  model: result.model.display_name,
  provider: result.model.provider,
  score: result.average_ccs,
  maxScore: 12,
  date: new Date(result.created_at).toLocaleString('en-US', { month: 'short', year: 'numeric', timeZone: 'UTC' }),
  releaseLabel: result.model.version || 'audit-run',
  prompts: result.prompt_count,
  ratings: result.total_ratings,
  status: result.human_review_required ? 'provisional' : 'validated',
  source: `CAREVAL audit run ${result.run_id}`,
  domainScores: {
    hr: result.average_ccs,
    healthcare: result.average_ccs,
    product: result.average_ccs,
    government: result.average_ccs,
    education: result.average_ccs,
  },
}));

const fileBody = `// Generated by scripts/publish-leaderboard.mjs
// Review before replacing src/app/data/leaderboard.ts.

export const generatedLeaderboardData = ${JSON.stringify(entries, null, 2)};
`;

const outputPath = path.join(repoRoot, 'audits', 'leaderboard.generated.ts');
await writeFile(outputPath, fileBody, 'utf8');

console.log(`Wrote candidate leaderboard to audits/leaderboard.generated.ts`);
console.log('Review it before copying entries into src/app/data/leaderboard.ts.');
