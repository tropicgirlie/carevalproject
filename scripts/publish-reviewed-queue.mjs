#!/usr/bin/env node
import { writeFile } from 'node:fs/promises';
import path from 'node:path';
import { readJson, repoRoot, slugify, summarizeRatings, writeJson } from './audit-lib.mjs';

const queuePath = process.argv[2];
if (!queuePath) {
  console.error('Usage: npm run leaderboard:publish-reviewed -- audits/review-queues/<run-id>.reviewed.json');
  process.exit(1);
}

const queue = await readJson(queuePath);
const items = queue.items ?? [];
const unreviewed = items.filter((item) => item.review_status !== 'reviewed');

if (unreviewed.length > 0) {
  console.error(`Cannot publish: ${unreviewed.length} queue item(s) are not marked reviewed.`);
  console.error('Open the admin review page, review every item, export reviewed JSON, then run this command again.');
  process.exit(1);
}

const byModel = new Map();

for (const item of items) {
  const summary = summarizeRatings(item.ratings);
  const reviewedItem = {
    ...item,
    ...summary,
    human_review_required: false,
    reviewed_at: item.reviewed_at ?? new Date().toISOString(),
  };

  if (!byModel.has(item.model.id)) {
    byModel.set(item.model.id, {
      model: item.model,
      prompt_results: [],
    });
  }

  byModel.get(item.model.id).prompt_results.push(reviewedItem);
}

const modelResults = [];

for (const [modelId, modelRun] of byModel.entries()) {
  const totals = modelRun.prompt_results.map((result) => result.total_score);
  const average = totals.reduce((sum, score) => sum + score, 0) / totals.length;
  const result = {
    schema: 'careval-model-audit-result-v1',
    run_id: queue.run_id,
    created_at: new Date().toISOString(),
    review_status: 'reviewed',
    human_review_required: false,
    model: modelRun.model,
    prompt_count: modelRun.prompt_results.length,
    total_ratings: modelRun.prompt_results.length * 6,
    average_ccs: Number(average.toFixed(2)),
    average_cbi: Number((12 - average).toFixed(2)),
    threshold: 9,
    threshold_met: average >= 9,
    prompt_results: modelRun.prompt_results,
  };

  modelResults.push(result);
  await writeJson(`audits/results/${queue.run_id}/${slugify(modelId)}.reviewed.json`, result);
}

modelResults.sort((a, b) => b.average_ccs - a.average_ccs);

const entries = modelResults.map((result, index) => ({
  rank: index + 1,
  model: result.model.display_name,
  provider: result.model.provider,
  score: result.average_ccs,
  maxScore: 12,
  date: new Date(result.created_at).toLocaleString('en-US', { month: 'short', year: 'numeric', timeZone: 'UTC' }),
  releaseLabel: result.model.version || 'reviewed-audit',
  prompts: result.prompt_count,
  ratings: result.total_ratings,
  status: 'validated',
  source: `CAREVAL reviewed audit run ${result.run_id}`,
  domainScores: {
    hr: result.average_ccs,
    healthcare: result.average_ccs,
    product: result.average_ccs,
    government: result.average_ccs,
    education: result.average_ccs,
  },
}));

const fileBody = `// Generated by scripts/publish-reviewed-queue.mjs
// Source: ${queuePath}

export const generatedLeaderboardData = ${JSON.stringify(entries, null, 2)};
`;

const outputPath = path.join(repoRoot, 'audits', 'leaderboard.generated.ts');
await writeFile(outputPath, fileBody, 'utf8');

console.log(`Wrote reviewed model results to audits/results/${queue.run_id}/`);
console.log('Wrote reviewed candidate leaderboard to audits/leaderboard.generated.ts');
